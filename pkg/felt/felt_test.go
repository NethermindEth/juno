type testPairFelt struct {
	element Felt
	bigint  big.Int
}

func (z *Felt) biggerOrEqualModulus() bool {
	if z[3] > qFelt[3] {
		return true
	}
	if z[3] < qFelt[3] {
		return false
	}

	if z[2] > qFelt[2] {
		return true
	}
	if z[2] < qFelt[2] {
		return false
	}

	if z[1] > qFelt[1] {
		return true
	}
	if z[1] < qFelt[1] {
		return false
	}

	return z[0] >= qFelt[0]
}

func gen() gopter.Gen {
	return func(genParams *gopter.GenParameters) *gopter.GenResult {
		var g testPairFelt

		g.element = Felt{
			genParams.NextUint64(),
			genParams.NextUint64(),
			genParams.NextUint64(),
			genParams.NextUint64(),
		}
		if qFelt[3] != ^uint64(0) {
			g.element[3] %= (qFelt[3] + 1)
		}

		for g.element.biggerOrEqualModulus() {
			g.element = Felt{
				genParams.NextUint64(),
				genParams.NextUint64(),
				genParams.NextUint64(),
				genParams.NextUint64(),
			}
			if qFelt[3] != ^uint64(0) {
				g.element[3] %= (qFelt[3] + 1)
			}
		}

		g.element.ToBigIntRegular(&g.bigint)
		genResult := gopter.NewGenResult(g, gopter.NoShrinker)
		return genResult
	}
}

func genFull() gopter.Gen {
	return func(genParams *gopter.GenParameters) *gopter.GenResult {

		genRandomFq := func() Felt {
			var g Felt

			g = Felt{
				genParams.NextUint64(),
				genParams.NextUint64(),
				genParams.NextUint64(),
				genParams.NextUint64(),
			}

			if qFelt[3] != ^uint64(0) {
				g[3] %= (qFelt[3] + 1)
			}

			for g.biggerOrEqualModulus() {
				g = Felt{
					genParams.NextUint64(),
					genParams.NextUint64(),
					genParams.NextUint64(),
					genParams.NextUint64(),
				}
				if qFelt[3] != ^uint64(0) {
					g[3] %= (qFelt[3] + 1)
				}
			}

			return g
		}
		a := genRandomFq()

		var carry uint64
		a[0], carry = bits.Add64(a[0], qFelt[0], carry)
		a[1], carry = bits.Add64(a[1], qFelt[1], carry)
		a[2], carry = bits.Add64(a[2], qFelt[2], carry)
		a[3], _ = bits.Add64(a[3], qFelt[3], carry)

		genResult := gopter.NewGenResult(a, gopter.NoShrinker)
		return genResult
	}
}

