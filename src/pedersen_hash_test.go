package main

import (
	"testing"

	"github.com/stretchr/testify/assert"
)

func test_pedersen_hash(t *testing.T) {
	a := pedersen_hash("2089986280348253421170679821480865132823066470938446095505822317253594081284", "1713931329540660377023406109199410414810705867260802078187082345529207694986")
	assert.Equal(t, "2006933912378029901530546741707610702800363803324550763092784179878473862240", a)
	b := pedersen_hash("996781205833008774514500082376783249102396023663454813447423147977397232763", "1668503676786377725805489344771023921079126552019160156920634619255970485781")
	assert.Equal(t, "3342270980645934216906357168482041155274269929902051783708905971058608379973", b)
}
